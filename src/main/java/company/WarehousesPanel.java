    private void jLabel2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel2MouseClicked
        String item = ((String)jComboBox1.getSelectedItem()).toLowerCase();
        String searchText = searchLabel.getText().trim().toLowerCase();
        ResultSet rs = null;
        PreparedStatement pstmt = null;
        Connection con = null;
        try{
            DriverManager.registerDriver(new oracle.jdbc.driver.OracleDriver());
            con = DriverManager.getConnection(Main.URL);
            switch(item){
                case "id":
                pstmt = con.prepareStatement("select id, location, area from warehouses where id = ?");
                pstmt.setLong(1, Long.parseLong(searchText));
                rs = pstmt.executeQuery();
                break;
                case "location":
                pstmt = con.prepareStatement("select id, location, area from warehouses where lower(location) like ?");
                pstmt.setString(1, "%"+searchText+"%");
                rs = pstmt.executeQuery();
                break;
                case "area":
                    pstmt = con.prepareStatement("select id, location, area from warehouses where area = ?");
                    pstmt.setInt(1, Integer.parseInt(searchText));
                    rs = pstmt.executeQuery();
                    break;
            }
        }
        catch(SQLException e){e.printStackTrace(); JOptionPane.showMessageDialog(this, "failed in connection to the database .", "error", JOptionPane.ERROR_MESSAGE);}
        catch(NumberFormatException e){System.err.println("NumberFormatException was caught .");}
        try{
            Main.buildTableModel(rs, jTable1);
            pstmt.close();
            con.close();
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, "nothing found");
            e.printStackTrace();
        }
    }//GEN-LAST:event_jLabel2MouseClicked

