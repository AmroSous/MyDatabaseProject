    private void kButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kButton4ActionPerformed
        if(jTable1.getSelectedRow() == -1){
            JOptionPane.showMessageDialog(this, "You must choose from the table", "Hint", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        DefaultTableModel dtm = (DefaultTableModel)jTable1.getModel();
        BigDecimal id = (BigDecimal)dtm.getValueAt(jTable1.getSelectedRow(), 1);
        Connection con = null;
        PreparedStatement ps = null;
        try{
            DriverManager.registerDriver(new oracle.jdbc.driver.OracleDriver());
            con = DriverManager.getConnection(Main.URL);
            ps = con.prepareStatement("select warehouse_id from stores where warehouse_id = ?");
            ps.setBigDecimal(1, id);
            ResultSet rs = ps.executeQuery();
            if(rs.next()){
                JOptionPane.showMessageDialog(this, "you can't delete this warehouse .. \n because it contains products", "WARNING", JOptionPane.WARNING_MESSAGE);
                ps.close();
                con.close();
                return;
            }
            if(JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this warehouse ?", "WARNING", JOptionPane.YES_NO_OPTION) == JOptionPane.NO_OPTION){
                ps.close();
                con.close();
                return;
            }
            ps = con.prepareStatement("delete from warehouses where id = ?");
            ps.setBigDecimal(1, id);
            ps.executeUpdate();
            dtm.removeRow(jTable1.getSelectedRow());
            JOptionPane.showMessageDialog(this, "The warehouse was deleted .", "Done", JOptionPane.INFORMATION_MESSAGE);
            ps.close();
            con.close();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this, "Failed in connection to the database.", "Oops!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_kButton4ActionPerformed

