    private void kButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kButton1ActionPerformed
        if(idField.getText().equals("")){JOptionPane.showMessageDialog(this, "Choose a warehouse from the table", "Hint", JOptionPane.INFORMATION_MESSAGE); return; }
        Long id = Long.parseLong(idField.getText());
        String location = locationField.getText().trim();
        int area;
        if(location.equals("")){
            JOptionPane.showMessageDialog(this, "Invalid location .", "Invalid Input", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(areaField.getText().trim().equals("")){
            JOptionPane.showMessageDialog(this, "Invalid area .", "Invalid Input", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Connection con = null;
        PreparedStatement stmt = null;
        try {
            area = Integer.parseInt(areaField.getText().trim());
            DriverManager.registerDriver(new oracle.jdbc.driver.OracleDriver());
            con = DriverManager.getConnection(Main.URL);
            stmt = con.prepareStatement("update warehouses set location = ?, area = ? where id = ?");
            stmt.setString(1, location);
            stmt.setInt(2, area);
            stmt.setLong(3, id);
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "The warehouse updated successfully .", "Done", JOptionPane.PLAIN_MESSAGE);
            findAllWarehouses();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Failed in connection to the database.", "Oops!", JOptionPane.ERROR_MESSAGE);
            return;
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Invalid area .", "Invalid input", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_kButton1ActionPerformed

