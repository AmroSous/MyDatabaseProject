    private void kButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kButton6ActionPerformed
        DefaultTableModel dtm1 = (DefaultTableModel) this.jTable1.getModel();
        int row = this.jTable1.getSelectedRow();
        if (row == -1) {
            return;
        }
        BigDecimal id = (BigDecimal) dtm1.getValueAt(row, 1);
        try {
            DriverManager.registerDriver(new oracle.jdbc.driver.OracleDriver());
            Connection con = DriverManager.getConnection(Main.URL);
            PreparedStatement stmt = con.prepareStatement("select stores.product_id, products.name, stores.quantity "
                    + "from stores inner join products on stores.product_id = products.id "
                    + "where stores.warehouse_id = ?");
            stmt.setBigDecimal(1, id);
            ResultSet rs = stmt.executeQuery();
            WarehouseContents contents = new WarehouseContents();
            Main.buildTableModel(rs, contents.table);
            int total = 0;
            dtm1 = (DefaultTableModel)contents.table.getModel();
            for(int i = 0; i < dtm1.getRowCount(); i++){
                total += Integer.parseInt(dtm1.getValueAt(i, 3).toString());
            }
            contents.label.setText(total + "");
            Home.glassPane.add(contents);
            Home.glassPane.setVisible(true);
            stmt.close();
            con.close();
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "failed in connection to the database .", "error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_kButton6ActionPerformed

