    public static void generateInvoice(String orderID) {
        try {
            DriverManager.registerDriver(new oracle.jdbc.driver.OracleDriver());
            
            Connection con = DriverManager.getConnection(Main.URL);
            InputStream input = new FileInputStream(new File("Invoice.jrxml"));
            JasperDesign jd = JRXmlLoader.load(input);

            PreparedStatement stmt = con.prepareStatement("select customers.id, customers.fName, customers.lName, "
                    + "orders.order_date, sum(products.price * orderitems.quantity) from customers, orders, products, orderitems "
                    + "where customers.id = orders.customer_id and products.id = orderitems.product_id and orderitems.order_id = orders.id "
                    + "and orders.id = ? group by (customers.id, customers.fName, customers.lName, orders.order_date)");
            stmt.setLong(1, Long.parseLong(orderID));
            ResultSet rs = stmt.executeQuery();
            if (rs.next()) {

                HashMap<String, Object> para = new HashMap<>();
                para.put("OrderId", Long.parseLong(orderID));
                para.put("OrderDate", rs.getDate(4).toString());
                para.put("CustomerId", String.valueOf(rs.getLong(1)));
                para.put("CustomerName", rs.getString(2) + " " + rs.getString(3));
                para.put("TotalPrice", String.valueOf(rs.getFloat(5)));
                para.put("OrderId", Long.parseLong("36"));

                JasperReport jr = JasperCompileManager.compileReport(jd);
                
                JasperPrint jp = JasperFillManager.fillReport(jr, para, con);

                JFrame frame = new JFrame("Invoice");
                frame.getContentPane().add(new JRViewer(jp));

                frame.pack();
                frame.setVisible(true);
                input.close();
                stmt.close();
                con.close();
        }
//            OutputStream os = new FileOutputStream(new File("testReport.pdf"));
//            JasperExportManager.exportReportToPdfStream(jp, os);
//            os.close();
//            input.close();

        } catch (SQLException e) {
            Logger.getLogger(AccountPanel.class.getName()).log(Level.SEVERE, null, e);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(AccountPanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (JRException ex) {
            Logger.getLogger(AccountPanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(AccountPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

