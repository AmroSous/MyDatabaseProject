    private void itemComboItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_itemComboItemStateChanged
        warehouseCombo.removeAllItems();
        warehousesIds.clear();
        spinner.setValue(0);
        if (itemCombo.getSelectedIndex() < 1) {
            return;
        }
        try {
            DriverManager.registerDriver(new oracle.jdbc.OracleDriver());
            Connection con = DriverManager.getConnection(Main.URL);
            PreparedStatement stmt = con.prepareStatement("select warehouses.id, warehouses.location from warehouses, "
                    + "stores where warehouses.id = stores.warehouse_id and stores.product_id = ?");
            stmt.setLong(1, productsIds.get(itemCombo.getSelectedIndex()));
            ResultSet rs = stmt.executeQuery();
            warehousesIds.add(-1);
            warehouseCombo.addItem("");
            while (rs.next()) {
                warehousesIds.add(rs.getInt(1));
                warehouseCombo.addItem(rs.getString(2));
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Failed in connection to the database .", "error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }

        warehouseCombo.setEnabled(true);

    }//GEN-LAST:event_itemComboItemStateChanged

