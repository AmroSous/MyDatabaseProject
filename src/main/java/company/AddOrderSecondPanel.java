    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        if ((int) spinner.getValue() == 0) {
            messageLabel.setText("please choose quantity .");
            return;
        }

        itemId.add(productsIds.get(itemCombo.getSelectedIndex()));
        warehouseId.add(warehousesIds.get(warehouseCombo.getSelectedIndex()));
        itemQuantity.add((Integer) spinner.getValue());
        totalPrice += productsPrices.get(itemCombo.getSelectedIndex());

        itemCombo.setSelectedIndex(0);
        warehousesIds.clear();
        warehouseCombo.removeAllItems();
        warehouseCombo.setEnabled(false);
        spinner.setEnabled(false);
        messageLabel.setText("");
        this.counter = counter + 1;
        counterLabel.setText("Item #" + counter);
        messageLabel.setText("Item " + (counter - 1) + " added ." );

    }//GEN-LAST:event_addButtonActionPerformed

