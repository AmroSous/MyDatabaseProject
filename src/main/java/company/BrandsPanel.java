    private void kButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kButton4ActionPerformed
        if(idField.getText().equals("")){JOptionPane.showMessageDialog(this, "Choose a brand from table", "Hint", JOptionPane.INFORMATION_MESSAGE); return; }
        Long id = Long.parseLong(idField.getText());
        String name = nameField.getText().trim();
        if(name.equals("")){
            JOptionPane.showMessageDialog(this, "Invalid name .", "Invalid Input", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Connection con = null;
        PreparedStatement stmt = null;
        try {
            DriverManager.registerDriver(new oracle.jdbc.driver.OracleDriver());
            con = DriverManager.getConnection(Main.URL);
            stmt = con.prepareStatement("select id from brands where lower(name) = ?");
            stmt.setString(1, name.toLowerCase());
            ResultSet rs = stmt.executeQuery();
            if(rs.next() && (rs.getLong(1) != id))
            {
                JOptionPane.showMessageDialog(this, "There is a brand with this name in the database .", "WARNING", JOptionPane.WARNING_MESSAGE);
                return;
            }
            stmt = con.prepareStatement("update brands set name = ? where id = ?");
            stmt.setString(1, name);
            stmt.setLong(2, id);
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "The brand updated successfully .", "Done", JOptionPane.PLAIN_MESSAGE);
            findAllBrands();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Failed in connection to the database.", "Oops!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_kButton4ActionPerformed

