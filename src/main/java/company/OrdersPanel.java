    private void kButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kButton6ActionPerformed

        try{
            DriverManager.registerDriver(new oracle.jdbc.driver.OracleDriver());
            Connection con = DriverManager.getConnection(Main.URL);
            PreparedStatement stmt = con.prepareStatement("select orders.id, orders.customer_id, customers.Fname, customers.Lname, orders.order_date"
                                + " from orders, customers where orders.customer_id = customers.id and "
                                + "orders.order_date between ? and ?");
            stmt.setDate(1, new java.sql.Date(afterCalendar.getDate().getTime()));
            stmt.setDate(2, new java.sql.Date(beforeCalendar.getDate().getTime()));
            ResultSet rs = stmt.executeQuery();
            Main.buildTableModel(rs, jTable1);
            stmt.close();
            con.close();
            
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Failed in connection to the database .", "Oops!", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_kButton6ActionPerformed

