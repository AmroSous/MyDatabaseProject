    private void kButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kButton10ActionPerformed
        DefaultTableModel dtm1 = (DefaultTableModel) this.jTable1.getModel();
        int row = this.jTable1.getSelectedRow();
        if (row == -1) {
            return;
        }
        BigDecimal id = (BigDecimal) dtm1.getValueAt(row, 1);
        try {
            DriverManager.registerDriver(new oracle.jdbc.driver.OracleDriver());
            Connection con = DriverManager.getConnection(Main.URL);
            PreparedStatement stmt = con.prepareStatement("select orderitems.product_id, products.name, products.price, "
                    + "orderitems.quantity, orderitems.warehouse_id FROM orderitems, products where "
                    + "orderitems.order_id = ? and orderitems.product_id = products.id");
            stmt.setBigDecimal(1, id);
            ResultSet rs = stmt.executeQuery();
            OrderItemsPanel itemsPanel = new OrderItemsPanel();
            Main.buildTableModel(rs, itemsPanel.table);
            float total = 0;
            dtm1 = (DefaultTableModel)itemsPanel.table.getModel();
            for(int i = 0; i < dtm1.getRowCount(); i++){
                total += Float.parseFloat(dtm1.getValueAt(i, 3).toString()) * Integer.parseInt(dtm1.getValueAt(i, 4).toString());
            }
            itemsPanel.label.setText(total + "");
            Home.glassPane.add(itemsPanel);
            Home.glassPane.setVisible(true);
            stmt.close();
            con.close();
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "failed in connection to the database .", "error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_kButton10ActionPerformed

